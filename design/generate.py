# ----------------------------------------------------------------------
# helpers
# ----------------------------------------------------------------------
def dps(atk, interval):
    return atk / interval if interval else 0

def power(hp, atk, interval):
    return round(hp * HP_W + dps(atk, interval) * DPS_W)

def real_strength(hp, atk, interval):
    return hp * HP_W + dps(atk, interval) * DPS_W

def nice_gold(x):
    if x < 100:   return int(round(x/10)*10)
    if x < 1000:  return int(round(x/50)*50)
    if x < 10000: return int(round(x/100)*100)
    return int(round(x/500)*500)

def write_csv(name, header, rows):
    p = os.path.join(OUT, name)
    with open(p, 'w', newline='', encoding='utf-8-sig') as f:
        w = csv.writer(f)
        w.writerow(header)
        w.writerows(rows)
    print('written', name, len(rows), 'rows')

