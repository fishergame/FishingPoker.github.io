# 3. 推荐初始属性（把越界卡拉回品质实力区间 + 战力统一公式）
# ----------------------------------------------------------------------
recommended = {}   # 序号 -> (hp, atk, interval, quality, power, real)
rows_base = []
for c in CARDS:
    idx, name, fac, rng, q, hp, atk, pw0, spd, iv, area = c
    if q == '普通' and atk == 0:  # 采矿机：仅按HP，无攻击
        lo, hi = POWER_BAND[q]
        r = hp * HP_W
        nh = hp
        if r < lo: nh = round(hp * (lo / r))
        recommended[idx] = (nh, 0, 0, q, 0, nh*HP_W)
        rows_base.append([idx, name, fac, rng, q, nh, '-', '-', spd, iv, area, '产金币'])
        continue
    lo, hi = POWER_BAND[q]
    r = real_strength(hp, atk, iv)
    factor = 1.0
    if r < lo: factor = lo / r
    elif r > hi: factor = hi / r
    nh = round(hp * factor)
    na = round(atk * factor)
    np_ = power(nh, na, iv)
    nr = real_strength(nh, na, iv)
    recommended[idx] = (nh, na, iv, q, np_, nr)
    note = ''
    if abs(factor-1) > 0.02:
        note = ('上调' if factor>1 else '下调') + f'{abs(factor-1)*100:.0f}%'
    rows_base.append([idx, name, fac, rng, q, nh, na, np_, spd, iv, area, note])

